mongoose.set('useNewUrlParser', true);
mongoose.set('useFindAndModify', false);
mongoose.set('useCreateIndex', true);
mongoose.set('useUnifiedTopology', true);

if (process.argv.length < 3) {
  console.log('give password as argument');
  process.exit(1);
}

